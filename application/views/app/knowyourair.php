<div class="col-md-12" style="padding: 36px;text-align: center;">
  <h2 style="color:#666666;font-size:18px;">Factors affecting your hair health</h2>
  <p style="color:#666666;font-size:12px;">Hair loss is presumably the most sensitive topic for those who are already dealing with it like soldiers. It takes a great deal of courage to portray the real side of hair loss. Here are a few factors that affect hair health.</p>
</div>

<div class="col-md-6" style="padding: 36px;">
  <h2 style="color:#666666;font-size:18px;">It's all in their genes</h2>
  <p style="color:#666666;font-size:12px;">some say that you got your eyes from your mother, hair from your father, smile from your sister as if it were something you can easily get from a grocery store. Jokes apart, apart from the hair type and colour, the genetic factors are also responsible for hair density, texture, and growth. Hereditary hair loss occurs with male and female pattern baldness. This type of baldness is caused due to the damage caused to hair follicles. In females, estrogen slows down hair growth and results in a thinner shaft of the hair. The estrogen levels also decrease after menopause leading to hair loss in women who are susceptible genetically.</p>
</div>

<div class="col-md-6" style="padding: 36px;">
  <h2 style="color:#666666;font-size:18px;">Cosmetics isn't always beautiful</h2>
  <p style="color:#666666;font-size:12px;">there are a few people who undergo cosmetic surgeries to enhance their looks, but they forget what it can do to their hair. Hair loss isn’t always caused because of losing an entire strand of hair, but in some conditions, it is also due to the breaking of hair strands. Some hair appliances and cosmetic procedures can cause hair damage and breakage. Excessive use of hairdryers, hair straighteners, curling devices can apply heat to the hair making them brittle. Excessive cleansing of your hair can also lead to hair loss as it may strip natural oils from the hair.
</p>
</div>

<div class="col-md-6" style="padding: 36px;">
  <h2 style="color:#666666;font-size:18px;">Stress beats them all</h2>
  <p style="color:#666666;font-size:12px;">If there was a contest of “Top reasons for hair loss”, Stress would eventually be the ultimate winner. Though it is considered to be unusual, severe psychological stress leads to a condition of temporary hair loss. Some studies also indicate that stress can impede the natural process of hair growth.</p>
</div>

<div class="col-md-6" style="padding: 36px;">
  <h2 style="color:#666666;font-size:18px;">Some have it in their hormones</h2>
  <p style="color:#666666;font-size:12px;">when it comes to the health & growth of your hair, both female and male hormones contribute significantly. In men, the major hormone that’s responsible for hair growth is androgen (to a small extent, in women too). The hormonal levels decrease as we age and lead to slow growth, hair thinning, and less lustrous hair. </p>
</div>


<div class="col-md-6" style="padding: 36px;">
  <h2 style="color:#666666;font-size:18px;">Those bagful of medications</h2>
  <p style="color:#666666;font-size:12px;">As we cross a certain age, our room will smell more medicines than essential oils. There are certain drugs that cause hair loss as a potential side effect. Keep a check on the medicines for arthritis, blood pressure, anticoagulants, antidepressants, oral contraceptives and Vitamin A supplements that may .</p>
</div>

<div class="col-md-6" style="padding: 36px;">
  <h2 style="color:#666666;font-size:18px;">No hair. No throne. </h2>
  <p style="color:#666666;font-size:12px;">As and how you age, your hair shafts weaken, the hair follicle and scalp also age along. The continuous impact of external factors such as pollutants, smoking, nutritional deficiencies, etc. and internal factors such as familial premature greying, androgenetic alopecia and medical conditions also affect hair health.
</p>
</div>

<div class="col-md-6" style="padding: 36px;">
  <h2 style="color:#666666;font-size:18px;">The pollution indeed</h2>
  <p style="color:#666666;font-size:12px;">There’s no shying away from pollution from any corner of the world. Every day, we are exposed to environmental pollutants such as dust, smoke, nickel, lead and arsenic, sulfur dioxide nitrogen dioxide, ammonia, polycyclic aromatic hydrocarbons (PAH) and volatile organic compounds. The list seems endless. The settling of these pollutants on the scalp leads to potential hair loss. Increased oxidative stress contributes to scalp irritation, redness, itching, excessive sebum secretion, dandruff, pain in the hair roots and hair loss.</p>
</div>

<div class="col-md-6" style="padding: 36px;">
  <h2 style="color:#666666;font-size:18px;">One drag can pull out endless strands</h2>
  <p style="color:#666666;font-size:12px;">It’s no surprise that smoking has been linked to hair loss and baldness. It confines the blood vessels and impacts blood flow throughout the body. The slowed-down blood flow in hair follicles hinders the growth of your hair. The hair follicles can also be damaged due to the toxins in the smoke which results in follicular inflammation due to the production of pro-inflammatory cytokines.</p>
</div>


<div class="col-md-6" style="padding: 36px;">
  <h2 style="color:#666666;font-size:18px;">How fertile is your scalp?</h2>
  <p style="color:#666666;font-size:12px;">The health of your scalp is very important. Several fungal diseases related to your scalp can contribute to slower hair growth. These conditions include dandruff, ringworm infection, alopecia areata, folliculitis, psoriasis and seborrheic dermatitis. These conditions can result in the inflammation of hair follicles and itchiness on the scalp. Itchiness is often linked with flakiness, scaly patches and hair loss. </p>
</div>

<div class="col-md-6" style="padding: 36px;">
  <h2 style="color:#666666;font-size:18px;">What you eat, goes to your head</h2>
  <p style="color:#666666;font-size:12px;">yes, it’s quite literal in this case. Your diet majorly dictates your hair health because hair is made of a natural protein called keratin. When you consume a balanced diet that’s rich in essential nutrients, it keeps your hair healthy. Proteins, along with vitamins, also trigger the hair cycle.
</p>
</div>



<div class="col-md-6" style="padding: 36px;">
  <h2 style="color:#666666;font-size:18px;">All hair products aren't safe</h2>
  <p style="color:#666666;font-size:12px;">There are a few hair products that contain harmful chemicals. These chemicals weaken and damage hair, resulting in temporary hair loss. While there are shampoos that contain polyethylene glycol and alcohol which make hair brittle, there are hair dyes that contain paraphenylenediamine causing scalp irritation and allergic reactions.
</p>
</div>


